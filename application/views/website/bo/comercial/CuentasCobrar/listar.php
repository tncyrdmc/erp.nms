function estado_cobro(venta,historial, usuario)
{
	var msg = "¿Esta seguro de confirmar el pago?";
	var	titulo = "Confirmacion de pago";

	bootbox.dialog({
		message: msg,
		title: titulo,
		buttons: {
			success: {
			label: "Aceptar",
			className: "btn-success",
			callback: function() {
				
				$.ajax({
					type: "POST",
					url: "/bo/cuentasporcobrar/cambiar_estado",
					data: {
						id_venta:venta, 
						id_historial: historial,
						id_usuario: usuario
					},
					}).done(function( msg )
							{
								bootbox.dialog({
									message: msg,
									title: "Atención",
									buttons: {
										success: {
										label: "Ok!",
										className: "btn-success",
										callback: function() {
											window.location="/bo/cuentasporcobrar/index";
											}
										}
									}
								});
							
						})

				}
			},
				danger: {
				label: "Cancelar!",
				className: "btn-danger",
				callback: function() {

					}
			}
		}
	})
	
	
	}
